function simplifyCategoryLabel(value) {
  return String(value ?? "")
    .trim()
    .toLowerCase()
    .replace(/[/>:|,-]+/g, " ")
    .replace(/&/g, " and ")
    .replace(/\s+/g, " ")
    .trim();
}

