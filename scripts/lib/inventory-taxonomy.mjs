export function canonicalizeInventoryCategory(value) {
  const trimmed = String(value ?? "").trim();
  if (!trimmed) {
    return null;
  }

  const simplified = simplifyCategoryLabel(trimmed);
  const exactMatch = exactCategoryMap.get(simplified);
  if (exactMatch) {
    return exactMatch;
  }

  const matcher = categoryMatchers.find((entry) => entry.match(simplified));
  if (matcher) {
    return matcher.value;
  }

  return trimmed.replace(/\s+/g, " ");
}
